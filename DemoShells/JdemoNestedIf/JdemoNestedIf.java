// Demo of Nested statements

public class JdemoNestedIf
{
	public static void main(String[] args)
	{
		int num = 10;

		// nested if example num in range 1..20

		if ( num >= 1 )
		{
			if ( num <= 20 )
			{
				System.out.println("Num is in the range 1..20\n");
			}
		}

	}  // main

} // JdemoNestedIf