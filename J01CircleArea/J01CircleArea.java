public class J01CircleArea
{
	public static void main(String[] args )
	{
		final int RADIUS = 2;

		final double PI = 3.14159;

		double areaCircle = PI * RADIUS * RADIUS;

		System.out.println();

		System.out.println("The Area of a Circle with a Radius of 2 is: " + areaCircle);

		System.out.println();

	} // main

} // J01CircleArea